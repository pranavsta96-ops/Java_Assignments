package Assignment2;

public class A4 {
	
	//Write a Java to display the multiplication table of a given integer using for loop.

	public static void main(String[] args) {

		int num =5;
		System.out.println("Table of"+ num + ": -");
		for(int a=1; a<=10; a++)
		{
			System.out.println(num+ "*"+ a+ "=" +num*a);
		}
		
		
	}

}
